/// A generated admin token: 32 raw bytes, base64url-encoded (no padding).
///
/// # Invariant (R-0008-a)
///
/// The inner string is ALWAYS 43 characters, URL_SAFE_NO_PAD alphabet.
/// Raw token bytes are accessible via `as_bytes()` (decoded) for hashing;
/// the encoded string is accessible via `as_str()` for file writes.
///
/// # Debug redaction
///
/// `Debug` is hand-written to emit `AdminToken(<redacted>)`. No `Display`
/// implementation exists — accidental format-string use will not compile.
pub struct AdminToken(String);

impl fmt::Debug for AdminToken {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "AdminToken(<redacted>)")
    }
}

impl AdminToken {
    /// The base64url-encoded token string (43 chars, no padding).
    pub fn as_str(&self) -> &str {
        &self.0
    }

    /// Decode the base64url token to its 32 raw bytes.
    ///
    /// Panics only if the token was constructed outside this module with an
    /// invalid base64url string — impossible via `generate()`.
    pub(crate) fn decoded_bytes(&self) -> [u8; 32] {
        let decoded = URL_SAFE_NO_PAD
            .decode(&self.0)
            .expect("AdminToken always contains valid base64url (generated by generate())");
        let mut buf = [0u8; 32];
        buf.copy_from_slice(&decoded);
        buf
    }
}

/// The BLAKE3 hash of an `AdminToken`'s raw bytes, for DB storage (R-0008-b).
///
/// The inner `[u8; 32]` is the 32-byte BLAKE3 hash output. Raw token bytes
/// are never exposed through this type.
#[derive(Debug, Clone)]
pub struct TokenHash(pub [u8; 32]);

impl TokenHash {
    /// The raw hash bytes for DB storage (BYTEA).
    pub fn as_bytes(&self) -> &[u8] {
        &self.0
    }
}

/// A row fetched from `admin_tokens` (mirrors the schema's six columns).
///
/// `token_hash` is a `Vec<u8>` to match BYTEA return from sqlx.
#[derive(Debug)]
pub struct TokenRow {
    pub id: Uuid,
    pub token_hash: Vec<u8>,
    pub workspace_id: Uuid,
    pub scopes: Vec<String>,
    pub created_at: DateTime<Utc>,
    pub rotated_at: Option<DateTime<Utc>>,
}

/// A rotation event emitted (as a return value) BEFORE the old row is deleted.
///
/// # Ordering contract (R-0008-g)
///
/// `rotate()` constructs this event from the old row's data before issuing
/// the DELETE. The event is returned to the caller; the caller observes it
/// before any DB state change is visible (both happen within one transaction,
/// but the return value logically "precedes" the committed deletion).
#[derive(Debug)]
pub struct TokenRotatedEvent {
    /// The rotated (old) token's `id`.
    pub token_id: Uuid,
    /// The workspace this token belongs to.
    pub workspace_id: Uuid,
}

/// Error type for rotation failures.
#[derive(Debug)]
pub enum RotateError {
    /// The old token row was not found by the given `old_token_id`.
    TokenNotFound(Uuid),
    /// A database error occurred during rotation.
    Db(sqlx::Error),
}

impl fmt::Display for RotateError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            RotateError::TokenNotFound(id) => {
                write!(f, "admin token not found: {id}")
            }
            RotateError::Db(e) => write!(f, "rotation db error: {e}"),
        }
    }
}

impl std::error::Error for RotateError {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        match self {
            RotateError::Db(e) => Some(e),
            RotateError::TokenNotFound(_) => None,
        }
    }
}

impl From<sqlx::Error> for RotateError {
    fn from(e: sqlx::Error) -> Self {
        RotateError::Db(e)
    }
}

