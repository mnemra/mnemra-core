/// Generate a fresh admin token: 32 bytes from OsRng → base64url-encode (no padding).
///
/// The returned token's string representation is exactly 43 characters,
/// URL_SAFE_NO_PAD alphabet (R-0008-a). The raw bytes are never stored;
/// callers use `hash()` for DB storage.
pub fn generate() -> AdminToken {
    let mut raw = [0u8; 32];
    // rand 0.10: OsRng is now SysRng (re-exported from getrandom).
    rand::rngs::SysRng
        .try_fill_bytes(&mut raw)
        .expect("SysRng (OS CSPRNG) must not fail");
    let encoded = URL_SAFE_NO_PAD.encode(raw);
    debug_assert_eq!(encoded.len(), 43, "base64url of 32 bytes must be 43 chars");
    AdminToken(encoded)
}

/// Hash token bytes for DB storage: BLAKE3(decoded_token_bytes) → 32-byte hash.
///
/// Accepts `&AdminToken` (not raw bytes) to prevent accidental hashing of the
/// base64-encoded string instead of the decoded bytes (R-0008-b).
pub fn hash(token: &AdminToken) -> TokenHash {
    let decoded = token.decoded_bytes();
    let hash_output = Blake3Hasher::new().update(&decoded).finalize();
    TokenHash(*hash_output.as_bytes())
}

/// Constant-time comparison of a presented token against a stored hash (R-0008-b).
///
/// # Constant-time contract
///
/// Uses `subtle::ConstantTimeEq` — the comparison is NOT short-circuitable by
/// the compiler. Using `==` on byte slices is explicitly forbidden here.
///
/// This function returns `true` if `BLAKE3(token.decoded_bytes()) == stored_hash.0`
/// in constant time.
pub fn verify(token: &AdminToken, stored_hash: &TokenHash) -> bool {
    let candidate = hash(token);
    // subtle::ConstantTimeEq::ct_eq returns Choice (0u8 or 1u8).
    // .into() converts Choice to bool.
    bool::from(candidate.0.as_ref().ct_eq(stored_hash.0.as_ref()))
}

/// Rotate a token: construct the rotation event from the old row, then execute
/// INSERT new + DELETE old in one transaction. Returns the new token and the
/// rotation event.
///
/// # Ordering (R-0008-g, R-0009-i)
///
/// The `TokenRotatedEvent` is constructed from the old row's data BEFORE the
/// DELETE is issued. Transaction ordering: INSERT new row first, then DELETE
/// old row, then commit. The event is returned; after return, `lookup_by_hash`
/// on the old hash returns `Ok(None)` with no grace period (R-0009-i).
///
/// # Pool note
///
/// The caller must supply a pool whose role has DELETE on `admin_tokens`.
/// In production that is the `mnemra_app` owner role. The `mnemra_host_fns`
/// role does NOT have DELETE — this is not widened here (see module doc).
pub async fn rotate(
    pool: &PgPool,
    old_token_id: Uuid,
    workspace_id: Uuid,
    scopes: Vec<String>,
) -> Result<(AdminToken, TokenRotatedEvent), RotateError> {
    // Fetch the old row to confirm it exists and to build the event.
    let old_row: Option<(Uuid,)> = sqlx::query_as("SELECT id FROM admin_tokens WHERE id = $1")
        .bind(old_token_id)
        .fetch_optional(pool)
        .await?;

    let _ = old_row.ok_or(RotateError::TokenNotFound(old_token_id))?;

    // Construct the rotation event BEFORE the DELETE (R-0008-g ordering).
    // The event carries the old token's id and workspace_id as its payload.
    let event = TokenRotatedEvent {
        token_id: old_token_id,
        workspace_id,
    };

    // Generate the new token and its hash.
    let new_token = generate();
    let new_hash = hash(&new_token);
    let new_id = Uuid::new_v4();

    // Execute: INSERT new, DELETE old — both in one transaction.
    let mut tx = pool.begin().await?;

    sqlx::query(
        "INSERT INTO admin_tokens (id, token_hash, workspace_id, scopes)
         VALUES ($1, $2, $3, $4)",
    )
    .bind(new_id)
    .bind(new_hash.as_bytes())
    .bind(workspace_id)
    .bind(&scopes)
    .execute(&mut *tx)
    .await?;

    // UPDATE rotated_at on old row before deleting — records rotation timestamp
    // for audit purposes (rotated_at is otherwise NULL for never-rotated tokens).
    sqlx::query("UPDATE admin_tokens SET rotated_at = now() WHERE id = $1")
        .bind(old_token_id)
        .execute(&mut *tx)
        .await?;

    sqlx::query("DELETE FROM admin_tokens WHERE id = $1")
        .bind(old_token_id)
        .execute(&mut *tx)
        .await?;

    tx.commit().await?;

    // Return event + new token. After this return, lookup_by_hash(old_hash) → Ok(None).
    Ok((new_token, event))
}

/// Look up a token row by its BLAKE3 hash.
///
/// Returns `Ok(None)` if not found. After rotation, a lookup by the old token's
/// hash returns `Ok(None)` immediately — no grace period (R-0009-i).
pub async fn lookup_by_hash(
    hash: &TokenHash,
    pool: &PgPool,
) -> Result<Option<TokenRow>, sqlx::Error> {
    let row = sqlx::query_as::<
        _,
        (
            Uuid,
            Vec<u8>,
            Uuid,
            Vec<String>,
            DateTime<Utc>,
            Option<DateTime<Utc>>,
        ),
    >(
        "SELECT id, token_hash, workspace_id, scopes, created_at, rotated_at
         FROM admin_tokens
         WHERE token_hash = $1",
    )
    .bind(hash.as_bytes())
    .fetch_optional(pool)
    .await?;

    Ok(row.map(
        |(id, token_hash, workspace_id, scopes, created_at, rotated_at)| TokenRow {
            id,
            token_hash,
            workspace_id,
            scopes,
            created_at,
            rotated_at,
        },
    ))
}

