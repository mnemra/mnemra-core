/// Write the admin token to a file at `path` with mode 0600 (R-0008-e).
///
/// The file is created (or truncated) with mode 0600 before content is written.
/// The create → chmod → write sequence ensures no window where the file is
/// readable at a wider mode.
///
/// # Test usage
///
/// Pass a path inside a `tempdir()` in tests. The `MNEMRA_TOKEN_FILE` env-var
/// resolution lives in `default_token_file_path()`.
pub fn write_token_file(token: &AdminToken, path: &Path) -> Result<(), std::io::Error> {
    use std::fs::OpenOptions;
    use std::io::Write;
    use std::os::unix::fs::{OpenOptionsExt, PermissionsExt};

    // Create parent directory if it does not exist.
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)?;
    }

    // Open with mode 0600 at creation time — no wider window.
    let mut file = OpenOptions::new()
        .write(true)
        .create(true)
        .truncate(true)
        .mode(0o600)
        .open(path)?;

    file.write_all(token.as_str().as_bytes())?;
    file.flush()?;

    // Explicit chmod ensures mode on an existing file being truncated is 0600.
    let permissions = std::fs::Permissions::from_mode(0o600);
    std::fs::set_permissions(path, permissions)?;

    Ok(())
}

/// Check that the token file at `path` has mode 0600 and is owned by the
/// current process UID (R-0008-e).
///
/// Returns `Err(TokenFileModeError::WrongMode)` if the file is world-readable
/// or group-readable (any bit beyond user read+write is set). Returns
/// `Err(TokenFileModeError::WrongOwner)` if the owner UID differs.
pub fn check_token_file_mode(path: &Path) -> Result<(), TokenFileModeError> {
    use std::os::unix::fs::MetadataExt;
    use std::os::unix::fs::PermissionsExt;

    let metadata = std::fs::metadata(path).map_err(TokenFileModeError::Io)?;

    let mode = metadata.permissions().mode() & 0o777;
    if mode != 0o600 {
        return Err(TokenFileModeError::WrongMode { actual: mode });
    }

    let file_uid = metadata.uid();
    let current_uid = libc_getuid();
    if file_uid != current_uid {
        return Err(TokenFileModeError::WrongOwner {
            file_uid,
            current_uid,
        });
    }

    Ok(())
}

/// Resolve the default token file path.
///
/// Path resolution order (R-0008-e):
/// 1. `MNEMRA_TOKEN_FILE` env var, if set and non-empty.
/// 2. `~/.config/mnemra/token` via HOME expansion.
///
/// Returns `None` if HOME is not set and `MNEMRA_TOKEN_FILE` is not set.
pub fn default_token_file_path() -> Option<PathBuf> {
    if let Ok(p) = std::env::var("MNEMRA_TOKEN_FILE")
        && !p.is_empty()
    {
        return Some(PathBuf::from(p));
    }
    let home = std::env::var("HOME").ok()?;
    Some(PathBuf::from(home).join(".config/mnemra/token"))
}

/// Thin wrapper around `libc::getuid()` to get the current process UID.
/// Isolated here to keep `unsafe` contained.
#[allow(unsafe_code)]
fn libc_getuid() -> u32 {
    // SAFETY: getuid() is always safe to call.
    unsafe { libc::getuid() }
}

