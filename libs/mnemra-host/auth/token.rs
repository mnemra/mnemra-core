/// Error returned by `check_token_file_mode`.
#[derive(Debug)]
pub enum TokenFileModeError {
    /// The file is not mode 0600 (world-readable or group-readable).
    WrongMode { actual: u32 },
    /// The file UID does not match the current process UID.
    WrongOwner { file_uid: u32, current_uid: u32 },
    /// An IO error occurred reading the file metadata.
    Io(std::io::Error),
}

impl fmt::Display for TokenFileModeError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            TokenFileModeError::WrongMode { actual } => {
                write!(
                    f,
                    "token file has wrong mode {actual:#o}, expected 0600 (R-0008-e)"
                )
            }
            TokenFileModeError::WrongOwner {
                file_uid,
                current_uid,
            } => {
                write!(
                    f,
                    "token file owner uid {file_uid} != current uid {current_uid} (R-0008-e)"
                )
            }
            TokenFileModeError::Io(e) => write!(f, "token file io error: {e}"),
        }
    }
}

impl std::error::Error for TokenFileModeError {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        match self {
            TokenFileModeError::Io(e) => Some(e),
            _ => None,
        }
    }
}

