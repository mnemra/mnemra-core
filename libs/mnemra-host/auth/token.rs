//! Admin-token generation, storage, and rotation (R-0008, R-0009).
//!
//! # Cryptographic choices
//!
//! - **Generation (R-0008-a):** 32 bytes from `rand::rngs::OsRng` (CSPRNG),
//!   base64url-encoded without padding via `base64::engine::general_purpose::URL_SAFE_NO_PAD`.
//!   The result is always exactly 43 characters.
//!
//! - **Hashing (R-0008-b):** BLAKE3(decoded_token_bytes) → 32-byte hash stored
//!   as BYTEA. The raw token bytes are never stored.
//!
//! - **Authentication (R-0008-b):** a presented token is authenticated by hashing
//!   it (`hash_presented` → BLAKE3) and looking the row up by that hash against the
//!   unique `admin_tokens.token_hash` column (`lookup_by_hash`). No constant-time
//!   byte comparison is performed or needed on this path: the admin token is a
//!   256-bit CSPRNG value (R-0008-a) and the match is a unique-index lookup on its
//!   BLAKE3 hash, so comparison-timing attacks do not apply. Constant-time
//!   comparison is still required where it does apply — the signing-chain
//!   verification path per P-0005 (see `signing/verify.rs`), not here.
//!
//! - **No logging of token material:** `AdminToken` has a hand-written `Debug`
//!   that emits only `AdminToken(<redacted>)`. No `Display` impl exists.
//!
//! # Rotation ordering guarantee (R-0008-g, R-0009-i)
//!
//! `rotate()` constructs `TokenRotatedEvent` from the old row's data BEFORE
//! issuing the DELETE. Transaction ordering: new token INSERT first, then old
//! token DELETE. The event value is returned BEFORE the caller can observe any
//! change in the DB — the return of `(AdminToken, TokenRotatedEvent)` is the
//! observable ordering seam. After `rotate()` returns, a `lookup_by_hash` on
//! the old hash returns `Ok(None)` — no grace period (R-0009-i).
//!
//! # DELETE grant note
//!
//! `rotate()` accepts a `&PgPool` supplied by the caller. In tests this is
//! `engine.pool` (the mnemra_app role, which owns the tables). The `mnemra_host_fns`
//! role has no DELETE grant at V0 (per cross-dispatch note 4). No DELETE grant
//! is widened here. A caller providing a pool without DELETE will receive a
//! sqlx error — that is correct behavior and must be handled by the caller.
//!
//! # File-mode mechanics (R-0008-e)
//!
//! `write_token_file(token, path)` creates/truncates the file at `path` with
//! mode 0600 before writing content (create → chmod → write sequence on Unix).
//! `check_token_file_mode(path)` returns a structured error if the file is
//! world-readable (mode & 0o177 != 0) or the UID does not match.
//!
//! Path resolution for the default path lives in `default_token_file_path()`:
//! reads `MNEMRA_TOKEN_FILE` env var, falls back to `~/.config/mnemra/token`.

use base64::{Engine, engine::general_purpose::URL_SAFE_NO_PAD};
use blake3::Hasher as Blake3Hasher;
use chrono::{DateTime, Utc};
use rand::TryRng;
use sqlx::PgPool;
use uuid::Uuid;

use std::fmt;
use std::path::{Path, PathBuf};

